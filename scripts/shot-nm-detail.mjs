import { chromium } from "playwright";
const browser = await chromium.launch();
const page = await browser.newPage({ viewport: { width: 1440, height: 1000 } });
await page.goto("http://localhost:3000/tr/projeler/neo-maison", { waitUntil: "domcontentloaded", timeout: 60000 });
await page.waitForTimeout(4000);
await page.screenshot({ path: "scripts/nm_detail_top.jpg", type: "jpeg", quality: 84 });
await page.evaluate(() => window.scrollTo(0, 1400));
await page.waitForTimeout(3000);
await page.screenshot({ path: "scripts/nm_detail_mid.jpg", type: "jpeg", quality: 84 });
await browser.close();
console.log("done");
process.exit(0);
